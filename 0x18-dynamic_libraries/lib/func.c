#include <stdio.h>

/**
 * add - performs addition operation
 * @a: first integer
 * @b: second integer
 *
 * Return: result of operation
 */

int add(int a, int b)
{
	return (a + b);
}



/**
 * sub - performs substraction operation
 * @a: first integer
 * @b: second integer
 *
 * Return: result of operation
 */

int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - performs multiplication operation
 * @a: first integer
 * @b: second integer
 *
 * Return: result of operation
 */

int mul(int a, int b)
{
	return (a * b);
}



/**
 * div - performs division operation
 * @a: first integer
 * @b: second integer
 *
 * Return: result of operation
 */

int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - performs modulus operation
 * @a: first integer
 * @b: second integer
 *
 * Return: result of operation
 */

int mod(int a, int b)
{
	return (a % b);
}
